public class Wedge {

    int A;

    int B;

    int C;

    public Wedge(int a, int b, int c) {
        A = a;
        B = b;
        C = c;
    }


    public int getA() {
        return A;
    }

    public int getB() {
        return B;
    }

    public int getC() {
        return C;
    }
}
